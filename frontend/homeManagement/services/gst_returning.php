                                                        <h4>1.) What is GST Return?</h4>
                                                            <p>A GST return is a document containing details of all income/sales and/or expense/purchase which a taxpayer (every GSTIN) is required to file with the tax administrative authorities. This is used by tax authorities to calculate net tax liability.
                                                                    Under GST, a registered dealer has to file GST returns that broadly include:</p>
                                                                    <ul>Purchases</ul>
                                                                    <ul>Sales</ul>
                                                                    <ul>Output GST (On sales)</ul>
                                                                    <ul>Input tax credit (GST paid on purchases)</ul>
