                                                        <h4>2. Who should file GST Returns?</h4>
                                                            <p>In the GST regime, any regular business having more than Rs.5 crore as annual aggregate turnover has to file two monthly returns and one annual return. This amounts to 26 returns in a year.
                                                                The number of GSTR filings vary for quarterly GSTR-1 filers under QRMP scheme. The number of GSTR filings online for them is 9 in a year, including the GSTR-3B and annual return.
                                                                There are separate returns required to be filed by special cases such as composition dealers whose number of GSTR filings is 5 in a year.</p>
                                                        
